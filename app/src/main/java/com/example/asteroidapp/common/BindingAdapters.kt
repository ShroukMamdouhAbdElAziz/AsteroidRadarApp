@BindingAdapter("imgUrl")
fun loadImage(view :ImageView , picture :PictureOfDay?){
    if (picture != null){


        if (picture.mediaType == "image") {

            Picasso.get()
                .load(picture.url)
                .into(view)

            view.contentDescription = picture.title
        } else {

            view.contentDescription = view.context.getString(R.string.image_of_the_day)

        }

    }
    else {

        view.contentDescription = view.context.getString(R.string.this_is_nasa_s_picture_of_day_showing_nothing_yet)
    }
}
